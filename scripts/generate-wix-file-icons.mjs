/**
 * Generates src-tauri/wix/file-icons.wxs — a WiX fragment that:
 *
 *   1. Installs every unique ext-*.ico file into <InstallDir>/icons/ext/
 *   2. Overrides HKCR\TypeX.<ext>\DefaultIcon for each extension so Explorer
 *      shows the labeled icon when TypeX is the default handler.
 *
 * Structure: one <Component> per unique icon design. That component owns
 * both the File and every RegistryValue (for each extension sharing that
 * design). This satisfies WiX's ICE30 (each target file installed by
 * exactly one component) and ICE69 (registry values must be in the same
 * component as the file they reference).
 *
 * The output file is MACHINE-SPECIFIC because File Source paths are
 * absolute. It's gitignored. Auto-regenerated on every `tauri build` via
 * tauri.conf.json's `beforeBuildCommand`.
 */
import fs from "node:fs";
import path from "node:path";
import { createHash } from "node:crypto";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC_TAURI = path.resolve(__dirname, "..", "src-tauri");
const ICONS_DIR = path.join(SRC_TAURI, "icons", "ext");
const OUT_PATH = path.join(SRC_TAURI, "wix", "file-icons.wxs");

// Map of icon design name → every extension that uses it.
// `design` is the suffix in ext-<design>.ico.
const DESIGN_TO_EXTS = {
  md:       ["md", "markdown", "mdx"],
  txt:      ["txt"],
  docx:     ["docx"],
  odt:      ["odt"],
  rtf:      ["rtf"],
  html:     ["html", "htm"],
  epub:     ["epub"],
  rst:      ["rst"],
  adoc:     ["adoc", "asciidoc"],
  tex:      ["tex", "latex"],
  org:      ["org"],
  textile:  ["textile"],
  wiki:     ["wiki", "mediawiki"],
  muse:     ["muse"],
  t2t:      ["t2t"],
  ipynb:    ["ipynb"],
  bib:      ["bib"],
  fb2:      ["fb2"],
  opml:     ["opml"],
  man:      ["man"],
};

const PROGID_PREFIX = "TypeX."; // Tauri emits <ProgId Id="TypeX.<ext>" …>

const deterministicGuid = (seed) => {
  const h = createHash("sha256").update("typex-wix-v2:" + seed).digest("hex");
  return [
    h.slice(0, 8),
    h.slice(8, 12),
    h.slice(12, 16),
    h.slice(16, 20),
    h.slice(20, 32),
  ]
    .join("-")
    .toUpperCase();
};

const safeId = (s) => s.replace(/[^A-Za-z0-9]/g, "_");

const xmlEscape = (s) =>
  s
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");

// ---- Build ----
const designs = Object.entries(DESIGN_TO_EXTS);
const components = [];
const componentRefs = [];

for (const [design, exts] of designs) {
  const designId = safeId(design);
  const cmpId = `cmpIcon_${designId}`;
  const fileId = `fileIcon_${designId}`;
  const guid = deterministicGuid(`icon-cmp:${design}`);
  const src = path.join(ICONS_DIR, `ext-${design}.ico`);

  // The component owns:
  //   - the File
  //   - one RegistryValue per extension pointing at this file's DefaultIcon
  // The File stays KeyPath so Windows Installer can reference-count correctly.
  const regLines = exts.map((ext) => {
    const progId = PROGID_PREFIX + ext;
    return `        <RegistryValue Root="HKCR" Key="${xmlEscape(progId)}\\DefaultIcon" Type="string" Value="[#${fileId}]"/>`;
  }).join("\n");

  components.push(
    `      <Component Id="${cmpId}" Guid="${guid}">
        <File Id="${fileId}" Source="${xmlEscape(src)}" KeyPath="yes"/>
${regLines}
      </Component>`,
  );
  componentRefs.push(`    <ComponentRef Id="${cmpId}"/>`);
}

const extCount = designs.reduce((n, [, exts]) => n + exts.length, 0);

const fragment = `<?xml version="1.0" encoding="UTF-8"?>
<!--
  Auto-generated by scripts/generate-wix-file-icons.mjs — do NOT edit by hand.

  One <Component> per unique icon design. The component owns:
    - the .ico File (installed to <InstallDir>\\icons\\ext\\)
    - one RegistryValue per extension that uses the design, overriding
      HKCR\\TypeX.<ext>\\DefaultIcon to point at that installed file.

  Icons only become visible in Explorer when the user chooses TypeX as the
  default app for that extension (Windows policy: apps can't force defaults).

  ${designs.length} unique icon designs covering ${extCount} extensions.
-->
<Wix xmlns="http://schemas.microsoft.com/wix/2006/wi">
  <Fragment>
    <DirectoryRef Id="INSTALLDIR">
      <Directory Id="dirIcons" Name="icons">
        <Directory Id="dirIconsExt" Name="ext">
${components.join("\n")}
        </Directory>
      </Directory>
    </DirectoryRef>

    <ComponentGroup Id="FileTypeIcons">
${componentRefs.join("\n")}
    </ComponentGroup>
  </Fragment>
</Wix>
`;

fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
fs.writeFileSync(OUT_PATH, fragment, "utf8");

console.log(
  `Wrote ${OUT_PATH}\n  ${designs.length} components · ${extCount} extensions covered`,
);
